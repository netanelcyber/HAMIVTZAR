import { useMemo } from '@wordpress/element';
import { Connection } from '../../social-store/types';
import useFeaturedImage from '../use-featured-image';
import useImageGeneratorConfig from '../use-image-generator-config';
import useMediaDetails from '../use-media-details';
import { getValidationError, isMediaConvertible } from '../use-media-restrictions';

/**
 * Validates media for a single connection based on its media source.
 *
 * @param {Connection} connection - The connection to validate.
 * @return Validation result.
 */
export function useConnectionMediaValidation( connection: Connection ) {
	const featuredImageId = useFeaturedImage();
	const { isEnabled: sigEnabled } = useImageGeneratorConfig();

	const mediaSource = connection.media_source;

	let mediaId: number | undefined;
	let hasAttachedMedia = false;

	switch ( mediaSource ) {
		case 'sig':
			// SIG generates a valid PNG — no media ID to look up
			mediaId = undefined;
			break;
		case 'none':
			mediaId = undefined;
			break;
		case 'featured-image':
			mediaId = featuredImageId;
			break;
		case 'media-library':
		case 'upload-video':
		default:
			mediaId = connection.attached_media?.[ 0 ]?.id || featuredImageId;
			hasAttachedMedia = ( connection.attached_media?.length ?? 0 ) > 0;
			break;
	}

	const [ mediaDetails ] = useMediaDetails( mediaId );

	return useMemo( () => {
		// SIG images are auto-generated PNGs, always valid
		if ( mediaSource === 'sig' ) {
			return { error: null, isConvertible: false };
		}

		const hasAutoGeneratedImage = sigEnabled && connection.service_name === 'instagram-business';

		const error = getValidationError(
			mediaDetails.metaData,
			mediaDetails.mediaData,
			connection.service_name,
			hasAttachedMedia,
			hasAutoGeneratedImage
		);

		return {
			error: error ?? null,
			isConvertible: isMediaConvertible( mediaDetails.metaData ),
		};
	}, [ mediaSource, sigEnabled, connection.service_name, mediaDetails, hasAttachedMedia ] );
}
