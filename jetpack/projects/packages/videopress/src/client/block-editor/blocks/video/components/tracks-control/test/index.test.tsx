import { render, screen } from '@testing-library/react';
import userEvent from '@testing-library/user-event';
import TracksControl from '..';

const mockInvalidateResolution = jest.fn();

jest.mock( '@wordpress/components', () => ( {
	ToolbarButton: ( { label, onClick } ) => <button onClick={ onClick }>{ label }</button>,
} ) );

jest.mock( '@wordpress/core-data', () => ( {
	store: 'core',
} ) );

jest.mock( '@wordpress/data', () => ( {
	useDispatch: () => ( { invalidateResolution: mockInvalidateResolution } ),
} ) );

jest.mock( '@wordpress/i18n', () => ( {
	__: ( text: string ) => text,
} ) );

jest.mock( '../../../../../../components/caption-manager-modal/lazy', () => ( {
	__esModule: true,
	default: ( { isOpen, isPrivate, onTracksChange } ) =>
		isOpen ? (
			<div role="dialog" aria-label="Manage subtitles" data-is-private={ String( isPrivate ) }>
				<button
					onClick={ () =>
						onTracksChange( [
							{
								kind: 'captions',
								srcLang: 'en',
								label: 'English',
								src: 'english.vtt',
								id: 42,
								status: 'ready',
								source: 'manual',
								isDraft: false,
								isAutoGenerated: false,
								isAutoSynced: false,
								downloadUrl: 'english.vtt?download=1',
								failureReason: '',
							},
						] )
					}
				>
					Update tracks
				</button>
			</div>
		) : null,
} ) );

jest.mock( '../../../../../../lib/url', () => ( {
	getVideoPressUrl: () => 'https://videopress.example/video',
} ) );

jest.mock( '../../icons', () => ( {
	tracksIcon: 'tracks',
} ) );

describe( 'TracksControl', () => {
	beforeEach( () => {
		jest.clearAllMocks();
	} );

	it( 'opens the shared subtitle manager modal from the block toolbar', async () => {
		const user = userEvent.setup();
		const setAttributes = jest.fn();

		render(
			<TracksControl
				attributes={ { guid: 'abc123', title: 'Test video', tracks: [] } }
				setAttributes={ setAttributes }
			/>
		);

		expect( screen.queryByText( 'Upload track' ) ).not.toBeInTheDocument();

		await user.click( screen.getByText( 'Manage subtitles' ) );
		expect( screen.getByRole( 'dialog', { name: 'Manage subtitles' } ) ).toBeInTheDocument();

		await user.click( screen.getByText( 'Update tracks' ) );

		/*
		 * The block attribute serializes into post markup, so only the historical
		 * `{ src, kind, srcLang, label }` shape may be persisted — none of the
		 * modal's processing-state fields.
		 */
		expect( setAttributes ).toHaveBeenCalledWith( {
			tracks: [
				{
					kind: 'captions',
					srcLang: 'en',
					label: 'English',
					src: 'english.vtt',
				},
			],
		} );
		expect( mockInvalidateResolution ).toHaveBeenCalledWith( 'getEmbedPreview', [
			'https://videopress.example/video',
		] );
	} );

	it( 'passes the video privacy through so private previews authenticate', async () => {
		const user = userEvent.setup();

		render(
			<TracksControl
				attributes={ { guid: 'abc123', title: 'Test video', isPrivate: true, tracks: [] } }
				setAttributes={ jest.fn() }
			/>
		);

		await user.click( screen.getByText( 'Manage subtitles' ) );

		expect( screen.getByRole( 'dialog', { name: 'Manage subtitles' } ) ).toHaveAttribute(
			'data-is-private',
			'true'
		);
	} );
} );
