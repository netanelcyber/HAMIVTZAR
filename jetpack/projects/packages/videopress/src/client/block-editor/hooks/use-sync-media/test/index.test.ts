const updateMediaHandler = jest.fn();

const baseAttributes: VideoBlockAttributes = {
	id: 123,
	guid: 'guid123',
	description: 'Chapter 1\n00:00 Intro',
	tracks: [],
};

/**
 * Find the setAttributes call that wrote a `tracks` value, if any.
 *
 * @param {jest.Mock} setAttributes - The setAttributes spy.
 * @return {Array|undefined} The matching call arguments, or undefined.
 */
function findTracksCall( setAttributes: jest.MockedFunction< VideoBlockSetAttributesProps > ) {
	return setAttributes.mock.calls.find(
		( [ arg ] ) => arg && Object.prototype.hasOwnProperty.call( arg, 'tracks' )
	);
}

/**
 * Render the hook through a save transition so the block-attributes => media
 * sync effect fires with `isOverwriteChapterAllowed` already settled to true.
 *
 * @return {object} The renderHook result and the setAttributes spy.
 */
function renderThroughSave() {
	const setAttributes = jest.fn() as jest.MockedFunction< VideoBlockSetAttributesProps >;

	/*
	 * First render: a save is in progress (no transition yet). This also lets
	 * the chapter-detection effect flip `isOverwriteChapterAllowed` to true.
	 */
	mockUseSelect.mockReturnValue( true );
	const utils = renderHook( () => useSyncMedia( baseAttributes, setAttributes ) );

	// Second render: saving has finished -> postHasBeenJustSaved becomes true.
	mockUseSelect.mockReturnValue( false );
	utils.rerender();

	return { ...utils, setAttributes };
}

beforeEach( () => {
	jest.clearAllMocks();

	mockUseSelect.mockReturnValue( false );
	mockUseDispatch.mockReturnValue( {
		invalidateResolution,
		__unstableMarkNextChangeAsNotPersistent: jest.fn(),
	} );
	mockUseVideoData.mockReturnValue( {
		videoData: {},
		isRequestingVideoData: false,
		videoBelongToSite: true,
	} );
	updateMediaHandler.mockResolvedValue( undefined );
	mockUseMediaDataUpdate.mockReturnValue( updateMediaHandler );
	mockUseVideoPosterData.mockReturnValue( { isGeneratingPoster: false } );
	mockValidateChapters.mockReturnValue( true );
} );

describe( 'useSyncMedia auto-generated chapter upload', () => {
	it( 'stores the normalized track from a structured upload response and invalidates the embed', async () => {
		mockUploadTrackForGuid.mockResolvedValue( {
			src: 'https://videos.files.wordpress.com/guid123/chapters.vtt',
			kind: 'chapters',
			srclang: 'en',
			label: 'English (auto-generated)',
		} );

		const { setAttributes } = renderThroughSave();

		await waitFor( () => expect( invalidateResolution ).toHaveBeenCalled() );

		const tracksCall = findTracksCall( setAttributes );
		expect( tracksCall ).toBeDefined();
		expect( tracksCall[ 0 ].tracks[ 0 ] ).toMatchObject( {
			kind: 'chapters',
			srcLang: 'en',
			src: 'https://videos.files.wordpress.com/guid123/chapters.vtt',
		} );
	} );

	it( 'short-circuits and surfaces an error when the upload resolves to a non-string error envelope', async () => {
		mockUploadTrackForGuid.mockResolvedValue( { error: 'upload_failed', message: 'nope' } );

		const { setAttributes, result } = renderThroughSave();

		await waitFor( () => expect( mockUploadTrackForGuid ).toHaveBeenCalled() );

		/*
		 * The failed upload must not be stored as a track src, and the embed
		 * resolution must not be invalidated.
		 */
		expect( findTracksCall( setAttributes ) ).toBeUndefined();
		expect( invalidateResolution ).not.toHaveBeenCalled();

		// The failure is surfaced to the consumer rather than silently dropped.
		await waitFor( () => expect( result.current.error ).toBeInstanceOf( Error ) );
		expect( result.current.error?.message ).toBe( 'nope' );
	} );

	it( 'surfaces an error when the upload rejects', async () => {
		mockUploadTrackForGuid.mockRejectedValue( new Error( 'network down' ) );

		const { setAttributes, result } = renderThroughSave();

		await waitFor( () => expect( result.current.error ).toBeInstanceOf( Error ) );
		expect( result.current.error?.message ).toBe( 'network down' );
		expect( findTracksCall( setAttributes ) ).toBeUndefined();
	} );

	it( 'stores the uploaded track and invalidates the embed when the upload resolves to a string', async () => {
		mockUploadTrackForGuid.mockResolvedValue(
			'https://videos.files.wordpress.com/guid123/track.vtt'
		);

		const { setAttributes } = renderThroughSave();

		await waitFor( () => expect( invalidateResolution ).toHaveBeenCalled() );

		const tracksCall = findTracksCall( setAttributes );
		expect( tracksCall ).toBeDefined();
		expect( tracksCall[ 0 ].tracks[ 0 ].src ).toBe(
			'https://videos.files.wordpress.com/guid123/track.vtt'
		);
	} );
} );
